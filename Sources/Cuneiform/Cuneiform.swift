// Cuneiform - Pure Swift library for Office Open XML SpreadsheetML (.xlsx) files
//
// This library provides reading and writing capabilities for Excel files
// following the ISO/IEC 29500 Office Open XML standard.

/// Library version information
public enum Cuneiform {
    /// The library version
    public static let version = "0.1.0"

    /// The supported Office Open XML version
    public static let ooXmlVersion = "ISO/IEC 29500 Transitional"
}

